# Train for classes

class Mycar:
    def __init__(self, vendor, model):
        self.vendor = vendor
        self.model = model


car1 = Mycar("Honda", "Fit")
print(car1.vendor)
print(car1.model)

class Person:
    def __init__(self, name, age):
        self.name = name
        self.age = age

p1 = Person("John", 36)

print(p1.name)
print(p1.age)